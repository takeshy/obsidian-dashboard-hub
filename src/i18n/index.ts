let currentLocale = "en";

export function getLocale(): string { return currentLocale; }

export function setLocale(locale: string): void {
  const normalized = locale.split("-")[0].toLowerCase();
  currentLocale = translations[normalized] ? normalized : "en";
}

export function initLocale(): void {
  try { setLocale(window.moment?.locale?.() || navigator.language || "en"); } catch { setLocale("en"); }
}

export function t(key: TranslationKey, vars?: Record<string, string | number>): string {
  let value = translations[currentLocale]?.[key] || translations.en[key] || key;
  if (vars) for (const [name, replacement] of Object.entries(vars)) value = value.split(`{{${name}}}`).join(String(replacement));
  return value;
}
